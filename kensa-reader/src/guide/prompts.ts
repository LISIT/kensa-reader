export function getDocType(id: DocType): DocTypeInfo {
  return DOC_TYPES.find((d) => d.id === id) ?? DOC_TYPES[DOC_TYPES.length - 1]
}

/** プロンプト全文（末尾に安全注意を必ず付ける） */
export function buildPrompt(id: DocType): string {
  return `${getDocType(id).prompt}\n\n※ ${SAFE_TAIL}`
}
